#include "ft_printf.h"

int	ft_put_str(char *s, int fd)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write (fd, "(null)", 6);
		return (0);
	}
	while (s[i])
		write(fd, &s[i++], 1);
	return (i);
}
